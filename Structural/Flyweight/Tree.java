public class Tree {

    private Integer x;
    private Integer y;
    private String name;
    
    public void setX(Integer x){
        this.x = x; 
    }
    
    public void setY(Integer y){
        this.y = y;
    }
    
    public void setName(String name){
        this.name = name;
    }
    
    public Integer getX(){
        return this.x;
    }
    
    public Integer getY(){
        return this.y;
    }
    
    public String getName(){
        return this.name;
    }
    
    
}